<script>
module.exports = {
    name: "Login",
    data: function () {
      return {
        loginData: {
            email: 'sandeshsatyal@gmail.com',
            password: 'sandy@123',
        },
        Authenticated: false,
       }
    },

    methods:
    {
        authenticate()
        {
            this.$http.post('http://socialtasking.dev/api/v1/login',this.loginData).then(function({data}){
                // $token = JSON.stringify(data);
                let token = data.token;
                localStorage.setItem("_token", token);
                let storedToken = localStorage.getItem("_token");
                if(storedToken)
                {
                    this.$set('Authenticated',true);
                    location.assign("http://socialtasking.dev");    
                }
                else
                {
                    this.$set('Authenticated',false);
                    this.$route.router.go('login');
                }
            });
        
        }
    },


 }
</script>